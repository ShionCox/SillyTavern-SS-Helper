/**
 * 功能：读取条目中心固定文案。
 * @param key 文案键名。
 * @returns 中文文案。
 */
export function resolveEntriesWorkbenchText(key: string): string {
    const normalized = String(key ?? '').trim();
    if (!normalized) {
        return '';
    }
    return ENTRIES_TEXT_MAP[normalized] ?? normalized;
}
