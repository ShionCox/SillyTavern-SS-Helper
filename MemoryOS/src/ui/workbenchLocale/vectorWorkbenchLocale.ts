/**
 * 功能：读取向量实验室固定文案。
 * @param key 文案键名。
 * @returns 中文文案。
 */
export function resolveVectorWorkbenchText(key: string): string {
    const normalized = String(key ?? '').trim();
    if (!normalized) {
        return '';
    }
    return VECTOR_WORKBENCH_TEXT_MAP[normalized] ?? normalized;
}
