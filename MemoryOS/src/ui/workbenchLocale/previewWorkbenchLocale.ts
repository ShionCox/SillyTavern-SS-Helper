/**
 * 功能：读取诊断中心固定文案。
 * @param key 文案键名。
 * @returns 中文文案。
 */
export function resolvePreviewWorkbenchText(key: string): string {
    const normalized = String(key ?? '').trim();
    if (!normalized) {
        return '';
    }
    return PREVIEW_WORKBENCH_TEXT_MAP[normalized] ?? normalized;
}
